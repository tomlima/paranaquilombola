            <h2>A História do Paraná</h2>
            <p>
                Durante muito tempo, a História do Paraná esteve assentada na ideia de que o Estado foi 
                construído por uma população branca, de origem europeia, sem que nenhuma relevância fosse dada às
                pessoas de origem africana que desde muito antes da imigração europeia já habitavam e trabalhavam na região. 
            </p>
