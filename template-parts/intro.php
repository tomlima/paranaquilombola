            <h2>Descortinando um outro passado</h2>
            <p>
                Desde o início dos anos 2000, uma série de iniciativas dos movimentos negros paranaenses e de políticas públicas 
                começaram a identificar os locais e descobrir as histórias das comunidades quilombolas do Paraná. Nesse contexto foi 
                criado o Grupo de Trabalho Clóvis Moura (GTCM), que durou de 2005 até 2010 e teve como objetivo realizar um levantamento
                das comunidades quilombolas paranaenses e identificar elementos referentes à educação, situação socioeconômica e possibilidade de 
                autorreconhecimento, conjugando ações de pesquisadores, governos e movimentos sociais. Coube também ao GTCM a articulação para a 
                efetivação das políticas públicas destinadas às comunidades, via reconhecimento da Fundação Cultural Palmares, órgão nacional 
                responsável pela emissão da certidão de autorreconhecimento. Esse é o processo pelo qual a comunidade se identifica como  grupo 
                étnico com tradições, histórias e culturas compartilhadas e relacionadas a determinada base territorial, mesmo que não fixa. 
                O autorreconhecimento ou autoidentificação são importantes, pois possibilitam à comunidade requerer a titulação das terras que ocupam.
                O GTCM identificou 36 comunidades quilombolas no Paraná, número que surpreendeu seus próprios membros. 
                O conhecimento dessas comunidades (e o número delas) foi ampliado desde então por diversos pesquisadores e pelos movimentos 
                internos de fortalecimento das comunidades e suas associações. Atualmente (agosto de 2020) estão identificadas 38 comunidades no Paraná, 
                sendo 36 certificadas pela Fundação Palmares e apenas uma titulada. Elas se formaram por diversos percursos históricos e se caracterizam
                também pela  diversidade de experiências. Acreditamos que o acesso a narrativas que contém a história dessas comunidades constitua 
                elemento importante para a construção identitária de seus membros e, principalmente, para a preservação de seus direitos.  
            </p>
